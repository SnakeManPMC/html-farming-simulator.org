<p>
Contracts, or missions as they were called in Farming Simulator 17 (FS17). See also <a href="../19/contracts.php">Farming Simulator 19 (FS19) Guide: Contracts</a> for basic information.
</p>

<p>
In Farming Simulator 22 (FS22) you can now by default have three (3) contracts running at the same time (without mods).
</p>

<p>
You should definitely get <a href="https://github.com/Mmtrx/FS22_BetterContracts" target="_blank">FS22 Better Contracts mod</a> which improves how contracts are handled, this is pretty much essential mod for anyone who does more than few contracts.
</p>

<p>
Better Contracts fixes the harvesting contracts which giants broke in some game engine patch, you have to adjust the percentage to deliver in order to accomplish harvest contracts.
</p>

<p>
Better Contracts adds icons to the ESC mapview on fields that have contract, it even includes icon type so you will recognize immediately cultivating, harvesting and fertilizing etc contract types.
</p>


	<h2>Accepting Contract</h2>

<p>
Press ESC to get into main FS22 menu. Then browse the horizontal GUI menu down to the icon which looks like two pieces of paper tilted next to each other (ugh my descriptions).
</p>

<img src="screenshots/thumbs/PMC-King-Corn-45km-2022-01-29T1547.jpg" alt="PMC King Corn 45km Contracts Menu Screenshot Farming Simulator 22 (FS22)">

<p>
You can use keyboard arrow keys or mouse scroll wheel to browse contract list up and down.
</p>

<p>
When you want to accept contract with borrowed vehicles press BACKSPACE button or click the "Borrow Items" button/icon/text at bottom of the screen. If you want to accept contract without borrowed vehicles then simply press SPACEBAR or click the "Accept Contract" thingy.
</p>

<p>
If you took borrowed vehicles they will appear at the vehicle shop aka the dealership yard. Once you have finished the contract and collect contract reward the vehicles (and any product in their tanks) will disappear, so its a good idea to get out of the vehicle first and possibly empty seed, fertilizer etc tank from any product. If you don't empty tanks then you are reimbursed money for that product.
</p>

<p>
Once you collect contract reward money is added to your account.
</p>

