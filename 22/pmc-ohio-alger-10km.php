	<h2>PMC Ohio Alger 10km</h2>

<p>
Terrain size: 10,240 meters x 10,240 meters, which is 10.2 kilometers x 10.2 kilometers (elmcreek is 2km). Terrain type: American, real world data. This terrain features <a href="https://www.twitch.tv/xr_renegade" target="_blank">xr-renegade (twitch)</a> streamer's home farm, as it is in real life Alger, OH, USA.
</p>

<p>
PMC Ohio Alger 8km real life location in <a href="https://www.google.com/maps/@40.6715957,-83.8524501,10775m/data=!3m1!1e3" target="_blank">google/maps</a>.
</p>

<div>
<p>
Click map image for more <a href="screenshots-pmc-ohio-alger-10km.php">screenshots</a>.
</p>
<a href="screenshots-pmc-ohio-alger-10km.php" target="_blank"><img src="screenshots/thumbs/PMC-Ohio-Alger-10km-Satellite.jpg" alt="Farming Simulator 22 Terrain - PMC Ohio Alger 10km Satellite Mapview" class="terrainimg" loading="lazy"></a>
</div>

<p>
Heightmap is done using Ohio state website downloaded 3DEP DEM tiles, these are incredibly detailed, oddly enough I cant find link to the actual resolution specifications, one location mentions 0.3 meter which is 30 CENTIMETERS (1 foot) resolution. This is obvious when you walk around in this terrain as road elevations and road ditches can be seen and they require no manual editing like we used to do back in FS19 early days.
</p>

<p>
Satellite imagery source to create PDA mapview is <a href="https://naip-usdaonline.hub.arcgis.com/" target="_blank">US National Agriculture Imagery Program (NAIP)</a>. Resolution is 1 meter / pixel.
</p>

