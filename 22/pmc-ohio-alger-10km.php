	<h2>Background</h2>

<p>
Twitch streamer xr-renegade is farming Alger, OH area, on january 26th 2023 xr-renegade did a twitch stream presentation about his farm, all the fields and everything included, even some other background, was great seeing that. Then we got to talk about creating real world data Farming Simulator 22 (FS22) terrain for this area.
</p>

<p>
Time went by and in march 3rd 2023 we really started to chat about creating this terrain. Rest is editing history which you can read from <a href="dev-diary-pmc-ohio-alger-10km.php">Developer Diary PMC Ohio Alger 10km</a>. Also check latest development from PMC forum topic <a href="https://www.pmctactical.org/forum/viewtopic.php?f=78&t=22862" target="_blank">PMC Ohio Alger 10km Development</a>.
</p>

<p>
PMC Ohio Alger 10km is the first PMC terrain which uses higher detailed heightmap than US NED 10m resolution.
</p>

<p>
We decided to publicly announce this terrain on 2023-03-10 xr-renegade's twitch live stream. This web page and official PMC forum topic went live after the twitch live stream announcement.
</p>

