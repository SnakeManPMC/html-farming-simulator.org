	<h2>2024-09-20 Creating Farm Yards</h2>

<p>
2024-09-20T03:07:00Z Was watching xr_renegade's youtube live stream where he played with similar small vehicles and equipment as he got in the real life farm, asked him to please list the equipment used and he did, here is what I wrote down.
