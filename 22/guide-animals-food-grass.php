To make <a href="guide-animals-food-silage.php">silage</a> from grass: plant grass, use tractor attached mowers or dedicated mower vehicle to mowe the grass once its ready to harvest. Then windrow the grass for easier baling, then bale it. If your baler had a wrapper then baling is done, if not use a wrapper implement to wrap the bales. Then use bale collector trailer and pick them all up, transport to where you want to store them. Note that once grass is wrapped into plastic its fermenting, it does not turn immediately into silage, it takes some time to ferment. There are various ways to turn grass into silage, this was just basic mowing, baling and wrapping.
