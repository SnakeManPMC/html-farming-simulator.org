	<h2>Fields</h2>

<p>
Fields smallest: 38.9 ha, largest 213.4 ha.
</p>

<p>
Please note that most of the 441 fields are 213ha in size, all boring identical squares, only the east and south terrain edge fields are smaller but that is only because they are sort of cut off by the terrain edge.
</p>

	<h2>Placeable Objects</h2>

<p>
New Farmer game-mode has big vehicle sheds, several grain bins and fertilizer bins by the vehicle shop. These can be used and sold.
</p>

	<h2>Game-play</h2>

<p>
Selling points: total of 12 selling points are distributed around the terrain. Selling points accept all grains and production chain products. Special animal dealer bale selling point is located by the vehicle shop area.
</p>

<p>
Contracts: AI does planting on numbered fields, contracts are available.
</p>

	<h2>Background</h2>

<p>
A very bad big map, with big square boring fields <img src="../images/smileys/icon_smile.gif" alt="Smiley :)" loading="lazy">
</p>

<p>
This terrain was not meant for playing as its a simply testing version for 32.7km x 32.7km terrain size, however it sort of developed on itself into quite nice big map with basic functionality. I find these completely square identical size fields extremely boring, but maybe someone who really likes LARGE HUGE fields farming finds some enjoyment from this terrain, thats why it got released. I really wish to have put some more irregular shapes and sizes of fields in, but oh well.
</p>

<p>
Heightmap is nice smooth rolling hills type elevations. There are over 400 square fields all the same size except on east and south edges, these fields have proper field definitions so contracts will work just fine as can be seen from screenshots.
</p>

<p>
There is a vehicle shop with sell/repair point. Farmland image is ... well there is some detail, I believe 36 individual land lot areas to purchase, but these are mostly 3000 hectares a piece costing whopping 29 million in average, so better save some money heh.
</p>

<p>
This terrain at the time of creation was the largest ever done for FS22, now obviously FS22 had been out just few weeks so it doesn't mean much. Goal of this terrain was to be the biggest one available, little did I know there would be something much more bigger and scarier in the horizon ... <img src="../images/smileys/icon_biggrin.gif" alt="Smiley :D" loading="lazy">
</p>

<p>
More editing details and thoughts from <a href="dev-diary-pmc-cereal-region-32km.php">Developer Diary PMC Cereal Region 32km</a>.
</p>

<p>
<i>The following paragraph was written in december 2021 and has now been outdated by <a href="pmc-korkscrew-40km.php">PMC Korkscrew 40km</a> and <a href="pmc-king-corn-45km.php">PMC King Corn 45km</a> terrain releases, but was left in-place as historical note:</i>
</p>

<p>
PMC Cereal Region 32km is absolutely megalodon gigantic size terrain, our previous <b>historical high score terrain size</b> was <a href="../19/pmc-eternal-sugar-beet-damnation-32km.php">PMC Eternal Sugar Beet Damnation 32km (FS19)</a> which did not work very well with low density image resolutions. Now FS22 has multi core and multi thread support which allow us to run huge density images and multiple vehicles (tested 4 tractors under Courseplay so far) at the same time without any freezes or even stutter, performance was 60FPS solid. What was a mere joke in FS19 is now pretty much the largest possible maybe even playable terrain in FS22. And that farmers, is a very good news <img src="../images/smileys/icon_smile.gif" alt="Smiley :)" loading="lazy">
</p>

