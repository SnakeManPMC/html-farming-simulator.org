	<h2>Homeless Paragraphs</h2>

<p>
After playing few days on PMC Farm Lab with animal placeables, its pretty clear to me now that animals exponentially increase farmsim time sink status. For example normal grain operations planting season + harvest season has a lot of time accelerating between these seasons, however if I were to add animals into such savegame it would reduce the "days of time accelerating" because depending on the number of animals you probably wont be able to time accelerate more than two days without feeding animals or emptying their manure, milk or slurry heaps / tanks. Farmsim is a time sink everyone should know that by now, but when animals enter the equation they look at you, then each other and say <i>"Hold my beverage..."</i> haha!
</p>

