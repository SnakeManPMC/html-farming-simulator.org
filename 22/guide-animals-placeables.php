	<h2>Animals Guide Placeables (FS22)</h2>

<p>
Giants hayloft vs bale storage placeables. Hayloft holds loose <a href="guide-animals-food-grass.php">grass</a>, <a href="guide-animals-food-hay.php">hay</a> or <a href="guide-animals-straw.php">straw</a> with 250,000 liter capacity, bale storage holds 250 bales of various types and sizes. If you place 250 large 180cm round bales of straw (11k liters each) into bale storage it comes to 250 * 11000 == 2,750,000 liters which is millions more than haylofts loose straw capacity. But please note that forage wagon collecting loose straw is super quick and easy compared to baling it and collecting those bales.
</p>

<p>
When you buy animal placeable and animals in there, it will take a while for animal status to be 0% health and reproduction, this will update after a while as game keeps running, don't panic.
</p>

<p>
GUI vertical navigation menu animals. Animals are listed in the batches they were bought, any animals reproduced (breeding, births) are repeated aka copy-pasted as categories in the animal menu. Meaning if you buy 1 <a href="guide-animals-sheep.php">sheep</a> and it reproduces then you have "sheep 1" and "sheep 1" categories in the menu, however if you buy lets say 25 sheeps at one go then you have "sheep 25" and when those reproduce you get another "sheep 25" in the menu. So in my limited experience opinion you need to decide if you want to be animal breeder and therefore buy amount of animals which suits the enclosure (barn, shed, pasture whatever) the best, for example sheep barn large has 65 animal capacity, you could divide 65 with lets say 3 to buy a breeder beginner set of animals which can breed twice before your animal enclosure if full and no more animals can be born until you sell or move some animals off. Overall point here is that if you have a large enclosure like sheep barn (large) with 65 animals and you buy single animal few times, what you end up with is a 65 entries on animal menu for "sheep 1" then, now that is a lot of scrolling, it soon becomes a nightmare to scroll that crazy long list. Who knows maybe optimum breeding for sell setup would be animal enclosure capacity / 2, now you can have animals born and then you just sell them away making room for another half of new animals. However after documenting this I found "chicken 8" which was hatched from "chicken 4" and a bit more later "chicken 12", whoah, that was, odd, can some animals reproduce more than a single new animal? Edit: even further out I now see "chicken 16" hmph, I don't get how <a href="guide-animals-chickens.php">chickens</a> reproduce... <img src="../images/smileys/icon_confused.gif" alt="Smiley :?" loading="lazy">
</p>

<p>
You can only buy and sell 60 animals without game-play enhancing mod, so lets say you have pig barn large which holds 270 animals, if you want to divide that to two sets of animals 270 / 2 == 135, you cant as max number of animals you can buy / sell is 60.
</p>

<p>
Straw storage. Even on small terrains with tiny fields you can have that many animals which require so much straw for bedding and for food (<a href="guide-animals-cattle.php">cattle</a> / <a href="guide-animals-cows.php">cows</a>, TMR mix) that you need a really large straw storage capacity. One large BALE storage placeable building is <a href="https://www.farming-simulator.com/mod.php?mod_id=237592" target="_blank">Pallet And Bale Warehouse mod</a> but this is bales, if you want easier access, transport and unloading you have to get forage wagon and loose straw. For loose straw you can use giants FS22 in-game hay loft under SHIFT-P -&gt; buildings -&gt; silos, hayloft capacity is 250k liters, this same menu also has bale & pallet storage which holds 250 bales.
</p>

