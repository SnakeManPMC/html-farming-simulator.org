	<h2>Build Mode</h2>

<p>
Build mode is feature under which placeables, landscaping and all related items are listed. Activate build mode by bringing up the SHOP menu, then bottom of the left side tool bar there are three horizontal (level) lines icon which is "Others" menu, from others menu select "Construction" page/icon thingy. Keyboard shortcut is SHIFT-P.
</p>

<p>
In the build menu you see kind of bottom part of the screen turn into a menu, there you have tabs: buildings, production, animals, decoration and landscaping.
</p>

<p>
Mouse wheel moves camera forward and back, when you press and hold mouse wheel while moving mouse you can pan camera around. To move camera use normal first person shooter WASD keys.
</p>

<p>
In buildings tab are further selections for: sheds, silos, silo extensions, container, tools and farmhouses.
</p>

<p>
In production tab are further selections for: factories, selling points, greenhouses, orchards and generators.
</p>

<p>
In animals tab are further selections for: cows, horses, pigs, sheep, chickens, bees and others.
</p>

<p>
In decoration tab are further selections for: fences, lights and others.
</p>

<p>
In landscaping tab are further selections for: sculpting, painting, trees and plants.
</p>


	<h2>Recommended FS22 Mods For Build Mode</h2>

<p>
Recommended mod for build mode is <b>FS22 Place Anywhere mod</b> which allows you to place objects, placeables, etc into anywhere regardless if you own the farmland or not. Also I believe there was some limitation in default game settings that you cannot place objects too close to existing terrain or placeable objects, this mod disables all such limitations.
</p>

<p>
You should use PlaceAnywhere mod responsibly of course, don't just go spamming weird object types in middle of where-ever, make wise choices where to put your placeables and such objects with the freedom that this mod gives you, with great power comes great responsibility.
</p>

<p>
If you like to landscape a lot, change terrain heightmap elevations, paint ground textures, place down shrubbery flowers, bushes and even trees, its highly recommended to get <b><a href="https://www.farming-simulator.com/mod.php?mod_id=225034" target="_blank">FS22 Free Landscaping Tools mod</a></b> as it will disable money being drained away from your account while landscaping. Landscaping should never cost money as game-play feature.
</p>

