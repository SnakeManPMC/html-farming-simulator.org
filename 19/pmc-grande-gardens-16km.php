	<h2>PMC Grande Gardens 16km</h2>

<p>
Terrain size: 16384 meters x 16384 meters, which is 16.3 kilometers x 16.3 kilometers (ravenport is 2km). Terrain type: American, fictional. No reference to any real world location, this is completely man made custom terrain. However it will have american style farming feeling of as everything is large scale and buildings will be american. This terrain was previously known as "MyMap_16km".
</p>

<div>
<p>
Click map image for <a href="screenshots-pmc-grande-gardens-16km.php" target="_blank">screenshots</a>.
</p>
<a href="screenshots-pmc-grande-gardens-16km.php" target="_blank"><img src="images/thumbs/PMC-Grande-Gardens-16km-PDA.jpg" alt="Farming Simulator 19 Terrain - PMC Grande Gardens 16km" loading="lazy" class="terrainimg"></a>
</div>

<p>
This terrain was ported from FS17, you might have seen videos from july 2018 where we demoed this terrain in action. This is the mega terrain with 2800 hectare field. I recall in FS17 I created the nice animal areas in this one (obviously not included in FS19 version). FS17 videos: PMC first ever 16.3km terrain in-game Farming Simulator 17 <a href="https://www.youtube.com/watch?v=-oHDt15nzUo" target="_blank">16384 Meter Size Terrain 2018-07-09</a> and <a href="https://www.youtube.com/watch?v=1V9m8Pm439M" target="_blank">PMC 16km Terrain Development 2018-07-10</a>. This was our most developed terrain in FS17 back in late summer of 2018. Would be nice to finish this for FS19.
</p>

<p>
Heightmap is brand new L3DT generated nice smooth rolling hills type with plenty of different elevations but still not too rough that farming vehicles and equipment can operate nice and easy. Some of the fields have breath taking views across the large farming landscape.
</p>

	<h2>Work In Progress</h2>

<p>
PMC Grande Gardens 16km terrain is work in progress (WIP), it is not finished yet. PMC is continuing to develop it, edit it along with our other terrains.
