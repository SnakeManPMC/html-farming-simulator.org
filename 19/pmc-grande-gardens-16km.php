<p>
PMC Grande Gardens 16km terrain has basic buildings, utility poles and trees placed. Basic object placement is complete, more tweaks follow after game-play testing has produced results.
</p>

<p>
If you do not want to play on WIP terrain please do not download this, there are plenty of other terrains available.
</p>

