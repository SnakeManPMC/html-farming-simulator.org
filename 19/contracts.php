Contracts (or missions as they were called in FS17).
</p>

<p>
In the easy mode (left most career start option) the contracts don't pay much, you get like lets say 44,000 which sounds on paper pretty good... but then you have to plant / harvest potatoes for 15 hectares which takes damn hours and hours...
</p>

<p>
There are some contracts which pay few hundreds and you got to be thinking that the balance is way off on these prices. Sure if you compare it to real life, a farmer would not pay you thousands to drive a tractor for a couple of hours but then again the money balance is off in farm sim games in general so you can't compare it to real life.
</p>

<p>
Contracts have no time limit, take as long as you want. World around you will not freeze anymore, so if you have fields with crops planted they do grow while you are doing contracts. You can use own vehicles / equipment or lease, if you lease the vehicles will de-spawn once the contract is completed.
</p>

<p>
In multiplayer you can work on contracts with other players. I've heard you can also contract jobs out for other players in the server (haven't tried this myself).
</p>

<p>
They do feel much better now than missions in FS17 mostly because no time limit and the world will not freeze around you.
</p>

