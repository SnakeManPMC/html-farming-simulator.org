	<h2>Work In Progress</h2>

<p>
PMC Texas Rowena 8km terrain terrain is work in progress (WIP), it is not finished yet. PMC is continuing to develop it, edit it along with our other terrains.
</p>

<p>
PMC Texas Rowena 8km terrain has basic buildings, utility poles and trees placed. Basic object placement is complete, more tweaks follow after game-play testing has produced results. See the above game-play 2020 screenshots for details.
</p>

<p>
If you do not want to play on WIP terrain please do not download this, there are plenty of other terrains available.
</p>

<p>
August 2020 I finished first ever savegame in FS19 which was in this terrains New Farmer game-mode. I played total of <b>566 hours</b> in it, got valuable game-play feedback and did several bug fixes, tweaks etc edits. This terrain has been extensively tested (come on 566hrs, just in this one savegame alone) and proven to be very enjoyable American real world data terrain :)
</p>

<p>
Plenty of <a href="https://www.youtube.com/user/pmcsince1984/search?query=%22PMC%20Texas%20Rowena%208km%22" target="_blank">youtube PMC Texas Rowena 8km development videos</a> available.
</p>

