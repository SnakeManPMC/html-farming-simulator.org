	<h2>2023-03-03 Heightmap Day</h2>

<p>
2023-03-03T20:26:00Z New editing day has started, today its a heightmap day.
</p>

<p>
USGS 1m heightmap download. Unfortunately global mapper heightmap view already tells me a grim story, yes there are incredible details ... but all the fields have terraces, I have not tried this in-game yet but I bet such terraces kill all the game-play as fields have crazy elevations, no idea how these farmers farm there with those terraces. But hey maybe I'm wrong and I need to check it in-game first?
</p>

<p>
heightScale="36.04"
</p>

<p>
Tested in-game and yeah there are cool details, no road ditches that I could find with quick run around, guess Rowena TX is just flat in that regard, terraces are on about every field, they can be easily seen but its maybe possible to air drill and combine over those terrace elevations, maybe. I bet its going to be clonky with 18.2m header or big 23m+ bourgault air drills, but yeah if you want realistic details now you got them, deal with it <img src="../images/smileys/icon_confused.gif" alt="Smiley :?" loading="lazy">
