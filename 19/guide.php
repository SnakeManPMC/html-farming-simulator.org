	<h2>Gameplay Tips</h2>

<p>
You can resize the user interface UI from the main menu options (not while game, ie savegame is running), like if you want to make it obnoxiously large or very tiny you can do that now. Nothing better than the new transparent UI style which is reduced in size, you hardly need hide HUD mod after this heh. If you make the UI smallest as possible, it also makes 9 key map windows quite small.
</p>

	<h2>Difficulty Options</h2>

<p>
Three different game modes for three difficulty levels at the same time.
</p>

<ul>
<li>Easy - New farmer</li>
<li>Normal - Farm-Manager</li>
<li>Hard - Start from scratch</li>
</ul>

<p>
New farmer: start with basic farm, fields, vehicles and equipment. Quite limited, small scale I would say.
</p>

<p>
Farm-manager: start with nothing except 1.250 million of money. You need to decide what land and vehicles to buy.
</p>

<p>
Start from scratch: start with nothing and very limited money, prices (and vehicle maintenance costs?) are the toughest.
</p>

<p>
Note: you can achieve the same thing by editing savegame directorys XML files like if you want to create "start from scratch" game mode for easy difficulty level. Considering this, the only unique or special thing in the new career game modes is the difficulty level.
</p>

	<h2>Plowing</h2>

<p>
You only need to plough after harvesting corn or sugar cane etc odd crops, there is no more plowing after every third harvest (thats for lime, see below). If you do not plant these odd crops then you never need to plow your fields.
</p>

	<h2>Lime</h2>

<p>
You can see lime field status on the map-view. Lime is a product which you purchase and spread to your fields using solid fertilizer spreader.
</p>

