	<h2>Downloading Mods Normally</h2>

<p>
Use your web browser to find a good FS19 mods website and download mods from there normally. Not sure what tutorial we can write about using world wide web <img src="../images/smileys/icon_wink.gif" alt="Smiley ;)" loading="lazy">
</p>

<p>
Be aware that Farming Simulator community has many bad websites which use scams to clickbait you into their pages which contain nothing but ads and no actual mod or whatever content you were looking for. Also many mod developers have only facebook pages for unknown reasons.
</p>

<p>
If the website looks okay and you found the mod you were looking for... be very aware of the download link, being it button or link text. If you cant see which site the download is coming from, hover mouse over the link and hopefully the link is shown in your browsers bottom left corner, sometimes not (these scam sites want to hide their intentions). Some file hosting sites are notoriously bad with "click this click that ads popup here" and you end up with several new browser tabs opened to shady advertisements or worse. You could try to see which file host site the link goes to and google it, over time you learn from experience which file hosts to avoid.
</p>

<p>
We do know several bad FS19 websites which should never be visited, but this list can change quickly, what is up to date today most likely is outdated couple of months from now as new scam sites are created. Its impossible to list "all safe websites" as bad ones can pop up in any moment.
</p>

<p>
Howto search FS19 mods from the web, use <a href="https://www.google.com/search?&q=farming+simulator+19+mods" target="_blank">google.com/search?&q=farming+simulator+19+mods</a> and you get plenty of hits, good and bad.
</p>

<p>
Once you have successfully downloaded mod ZIP file, place it on your mods directory as explained in beginning of this tutorial page. If you cannot find the download you should configure your web browser to download into directory you want, but most likely file is on "C:\Users\USERNAME\Downloads\" directory.
</p>

	<h2>What Are _UNZIP Files?</h2>

<p>
Sometimes mod developers especially terrain developers release "Filename_UNZIP.zip" packets, perhaps its RAR and unfortunately very rarely 7-zip. This is done wrong in almost all cases as terrain devs include required mods or "good stuff" which they think you should use to play in the downloaded release packet instead of a link, you probably already have them or they simply should be downloaded individually to save bandwidth and hard disk space.
</p>

<p>
"Filename_UNZIP.zip" can be opened with WinZip (windows 7/8/10 built in nowadays), WinRAR or 7-Zip.
</p>

<p>
"Filename_UNZIP.rar" and "Filename_UNZIP.7z" can be opened with WinRAR or 7-Zip.
</p>

<p>
If you already have WinRAR use it, but if you don't then download <a href="https://www.7-zip.org/" target="_blank">7-Zip</a> which is the best file compress software available in 2021-01-04.
</p>

<p>
Once you unpack zip/rar/7z archive you downloaded, bare minimum there should be mod ZIP files inside, also there might be readme or other files. Move only the FS19 mod zip files into your game mods directory and you are ready to play. Easiest way to identify actual mod zip file is to open it and if there is "modDesc.xml" file in the packet root directory, its a mod.
</p>

