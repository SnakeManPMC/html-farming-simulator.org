	<h2>Locating Mod Directory</h2>

<p>
By default MOD directory is located in "C:\Users\YOURNAME\Documents\My Games\FarmingSimulator2019\mods\".
</p>

<p>
You can configure where mod directly is located by editing gameSettings.xml file which is located in "C:\Users\YOURNAME\Documents\My Games\FarmingSimulator2019\" directory.
</p>

<p>
Below you see example gameSettings.xml file pointing mods directory to C:\FS19.Mods\ and please note that path by default is set with / instead of standard windows \ character.
</p>
<pre>
&lt;?xml version="1.0" encoding="utf-8" standalone="no" ?&gt;
&lt;gameSettings revision="15"&gt;
    &lt;modsDirectoryOverride active="true" directory="C:/FS19.Mods"/&gt;
</pre>
<p>
modsDirectoryOverride needs to be set to TRUE.
</p>

	<h2>Downloading Mods With In-Game Mods Menu</h2>

<p>
Easiest way to download limited number of mods is Giants ModHub from in-game "Mods" menu. Farming Simulator 19 (FS19) will automatically download mods what you select, then you only need to restart game to get the mods copied into mods directory, FS19 will do this automatically for you (don't worry it asks first).
