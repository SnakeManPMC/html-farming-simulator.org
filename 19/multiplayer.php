	<h2>Guide</h2>

<p>
<b>Chat</b>. To type into chat use T key. You can scroll chat log up/down by arrow keys. If you decide not to type in text, just hit ESC and chat dialog closes.
</p>

<p>
<b>My Ping</b>. Your ping is listed as milliseconds (ms), you can find it from the top right corner of mini map (hotkey 9). Average US to EU ping is about 100ms, this is perfectly fine for FS19 gaming.
</p>

<p>
<b>Being away</b>. If you go away like kitchen to get a beverage, restroom to take a leak or kitchen for a lunch, always exit any vehicle you're in. If you stay in a vehicle and are away, nobody else can use this vehicle. If you stay away for a very long time, this can be a real problem.
</p>

<p>
<b>Cruise Control</b>. Cruise control on vehicles keep a set speed until cruise control is disabled. Hotkeys 1 decrease speed, 2 increase speed, 3 engage and disengage cruise control.
</p>

<p>
<b>Crop Destruction</b>. Do not drive into growing crops as PMC server has crop destruction on. You can drive on fields that have no plants growing, you could even drive on them when plants grow on FIRST STAGE of growth, but on second stage I believe you start to destroy crops, so no driving on crops. Remember when you are combining that if you type into chat you cannot hit S or 3 keys to stop your combine, you need to ESC or enter out of the chat dialog first. Its very common to see combine becoming full and driving into crop destroying it while operator is typing into chat. Good rule of thumb is; stop typing into chat when you are 90 percent full.
</p>

<p>
<b>Combine operators responsibility</b> is not to drive on crops and take pipe out of the fruit when full, that's it. Combine does not chase grain cart, grain cart chases combine I guess thats why grain carts are also called chaser wagons. Combine operator need to pay attention when driving with GPS track, GPS will not stop the combine when its full, its extremely easy to drive into crop when getting full so you must pay close attention.
</p>

<p>
<b>GPS</b>. Very often new players have hard time using Wopster's GuidanceSteering mod aka GPS mod. Here is a very quick rundown how it works.<br>
<b>ALT-C</b> on / off. See the satellite icon in right side of the screen turn green, now GPS is on, white is off.<br>
<b>CTRL-S</b> menu. Menu only works when GPS is on.<br>
<b>ALT-E</b> start / end track. Use ALT-E first time to start recording track, drive forward the longer the better then use ALT-E second time to end the track.<br>
<b>ALT-HOME</b> center track. Centers current track along your vehicle.<br>
<b>ALT-END</b> rotate track 90 degrees.<br>
<b>ALT-X</b> enable / disable. This is how to actually drive with GPS, you hear a beep sound when its enabled/disabled. You can disabled GPS driving by steering left or right (A or D key, or steering wheel etc).
</p>

