	<h2>Guide and Gameplay Tips</h2>

	<h2>Gameplay Tips</h2>

<p>Relax. With real time scale, you are not in any hurry regarding crop growth, trust me you'll have plenty of time to wait after your shores are done (at least early in the game).
</p>

<p>On easy level at default goldcrest valley map your silo is full of valuable grains, sell them to get money. Check ESC menu prices/1000L listings which site pays the best.
</p>

<p>On career start, often your idea of good choice is not what the map / terrain maker had in mind, or maybe the map was meant as multiplayer map who knows, so you have too much stuff where you have no use for them. Well you can sell that stuff to get rid of it and also at the same time, get some money. You'll decide what you need at career start.
</p>

<p>Doors and fence gates can be opened by LMB pressed down all the way until it has opened.
</p>

<p>Night time starts around 2130hrs (at 2145hrs you need vehicle lights to operate) and daytime begins around 0450hrs. At dead of night depending on the weather conditions, it is pitch black and you can't see anything, even with vehicle lights its very disorienting to play game at night. But if you have clear weather with moonlight then you can see more or less enough.
</p>


	<h2>Difficulty Options</h2>

<p>
Easy, Normal and Hard. Basically the only difference is amount of money, loan and market prices including vehicle prices. You can change other settings from options menu, note that each difficulty level suggests specific settings for you, but they arent hard locking them.
</p>

<p>
People have said that difficulty level hard is just unecessary grinding for no good reason and you'd be better off playing on easy or normal instead where you get more money from selling crops.
</p>

<p>
Also some believe three stages of fertilization is just too much for a gameplay, it goes to too much grinding again for no reason, however if you only use one stage and seed with Big Bud seed hawk which also fertilizes (as some others do also), then you have no use for slurry and manure which would be a real shame because it is a big, a huge part of farming and farming simulator. Don't skip slurry and manure, they are great fertilization methods so just switch to 3 stage fertilization and enjoy the game without rushing to harvest.
</p>

<p>
Plant growth slow is really show, I mean <i>really</i> slow, so while fast might be bit too hectict action especially when you have more fields, slow is a bit slow indeed. Not sure, maybe normal is the best choice overall, although with large number or huge fields might drive you all the way up to (or down to?) slow.
</p>


	<h2>Plowing</h2>

<p>You don't have to plow after every harvest, only about after 3 harvests. Always check map overview if fields needs plowing or if you missed a spot.
</p>

<p>Missing a spot can easily happen even though the field looks like perfectly plowed.
</p>

<p>
With Big Bud DLC and the largest plow it is extremely difficult to do small fields or rather fields that have geometry blocking crap right next to it because Big Bud gets stuck for those obstacles so easily. It is incredible how much room this monster combination needs when they are turning at the end of the field. I once got my 747 stuck in a ditch on North West Texas 4X map because the turn radius with courseplay was so wide, I was lucky to drive that thing out of the ditch manually heh. Solution; use courseplay headland.
</p>

<p>
You can plow fields larger by pressing default Y key to turn allow making fields on/off. Note that with some mods installed this F1 control menu option disappears. When you plow fields larger, try to make the square so AI helpers / courseplay vehicles don't need to drive through geometry obstacles or other difficult spots. Manually enlargened fields do not show up in map overview image, only at the fruit types, growth and soil composition modes.
</p>

<p>
Its recommended to lease Big Bud 747 and SPSL 9 plow to enlargen any fields that have such possibility, because any harvests you do with such fields is free money. As soon as you have the lease money, do it and then return Big Bud so it wont cost you large sums of lease cash. Why lease Big Bug? Well its extremely fast 14.8km/h with 10.5m width to plow.
</p>

<p>
If your map don't fit Big Bud (if thats the case, change map haha), maybe you could get Salford 8312 plow and New Holland T9 with engine mod and twin wheels which is can pull the salford at 14km/h speeds.
</p>

	<h2>Sowing</h2>

<p>
Vaderstad Tempo F8 kind of sucks as it has huge load of fertilizer but very little seeds, so you have to constantly refill seeds and practically never the fertilizer (well at least it feels like it). Yeah sure its nice as it sows and fertilizes at one go, but man the seed tank is way too small heh.
</p>

	<h2>Seed Pallets</h2>

<p>When your sower / seeder runs out of seeds, you need to refill it. On starter easy level there are several seed pallets in your farm, but these run out and you have to purchase more.
</p>

<p>The shop has them for sale under Pallets. This will drop a pallet outside the shop in town. You then have to pick up the pallet and transport it back to your farm. For a single pallet the best way to transport it is to purchase a <b>front loader attachment</b> and the <b>pallet forks</b>. These can be attached to the front of any of your starting tractors. Once you have that you can pick up the pallet and move it.
</p>


	<h2>Seed Placeables</h2>

<p>
You can purchase placeable objects for seeds and fertilizer. These will always supply you with seeds / fertilizer without the need to purchase more and handle them, but it costs more than pallets.
</p>

<p>
When you purchase these, take into careful consideration where you place them. Do you plan on buying one than one set of these? If not, then its good idea to place them into location which is easily accessible and central location on the map.
</p>


	<h2>Train</h2>

<p>There are three massive buildings that can be accessed by the train. They are not marked locations on the map and can't be teleported to. Each one is a freely-availible silo with a 500K capacity that can hold all grain crops and root crops.
</p>

<p>The contents of these three silo buildings can be loaded or unloaded from any trailer or train car. Even without the train, each is a useful and free secondary storage location, but the storage figures in the menu don't include these silos, so if you stash some grain there remember that you did it.
</p>

<p>To load or unload a train, move a grain wagon (all three are mostly the same) under the pipe visible overhead, the discharge grate is lined up with it so loading and unloading are in the same spot.
</p>

<p>To load or unload a trailer from these buildings, look on the far side away from the tracks. There is a section of workshop that you can drive through, that's where the drop grate is, and there's a silo structure outside, that's where you can load. Despite appearances these sites will take potatoes and sugar beets.
</p>

<p><i>I use a train on big fields for 'field bin' storage - use an auger and you can move the train up the line while you unload. From the train you have several 500t hubs. At those hubs you can unload the train and sell to points all over the map as well as sell at Maplefield direct,</i>
</p>

<p><i>Learn the routes, the hubs and the truck/tipper access and you'll find they are a huge benefit to large game play.</i>
</p>

<p><i>Train is very useful. Can sell HUGE loads during great demands without the drop in price you would get between tipper loads.</i>
</p>

<p><i>Can also use it as intermediate storage as you harvet fields. Park a train close to the field being harvested, dump combine into auger wagon and unload directly into traincar...no need to run to silos.</i>
</p>

	<h2>Trailer</h2>

<p>Not a movie trailer ;-) but trailer which you hinch with tractor or car etc and you load various items on top of it for easy transport.
</p>

<p>For example seed pallets are ideal to be loaded into trailer in front of the shop and then driven to your farm.
</p>

<p>You need forklift to lift the stuff into a trailer.
</p>

	<h2>Fertilizing</h2>

<p>
After sowing a field you can increase its yield by fertilizing it. There are up to three stages of fertilization depending on your game difficulty level and options.
</p>

<p>
Check fertilization stages from ESC -> map overview -> soil composition -> fertilized.
</p>

<p>
Between each fertilization either the soil must be worked (cultivated, plowed, sown etc.) or the plants have to grow at least one stage before you can fertilize again.
</p>

<p>
Fertilization methods are; spreader, sprayer, manure spreader and slurry tanker.
</p>

<p>
Slurry manure is expended like crazy quickly. This is not very accurate, just very very rough estimate... but you probably need like 100,000 liters of liquid manure (slurry) to fertilize about 10-15 hectares of field. It is insane. Do not get into false ideas about not purchasing fertilizer and only using your animal shit, you need a lot, huge number of animals to produce decent amount of manure, we are talking about 500+ heads of pigs/cows.
</p>

<ul>
<li><b>1st fertilization:</b> Spray or solid fertilizer immediately after a crop harvest. You can do this before or after plowing / sowing.</li>
<li><b>2nd fertilization:</b> If your sower is a combo sower / fertilizer, and will lay down layer 2.</li>
<li><b>3rd Fertilization:</b> You can do this as soon as you see sprouts. I suggest a sprayer, but a solid fertilizer spreader will work fine here.</li>
</ul>

	<h2>Buying / Selling Equipment and Vehicles</h2>

<p>
You can sell equipment and vehicles from shop menu (P), you can sell them from anywhere but if you get the item into the shop, you get 20% more money.
</p>

<p>
Stay away from leased payments, they have incredibly high cost overall. If you must just stick it out until you have enough money to purchase whatever you need. Once you get fleet of leased vehicles, however super they might be for your poor ass, you quickly run into negative cash value as the costs keep running every day.
</p>

<p>
It is stupid to have the daily upkeep cost for vehicles, I mean when you have a car in real life it doesn't cost you 1000 euros every day, not even with the craziest insurances and taxes. So having this kind of game gimmick to make players "play" some stupid stuff is lame. So this is why you should not have excessive vehicles and equipment laying around because... it costs money just to keep them. Its all good fun to have nice toys in this game, but it costs money so you have to decide if you have the money to burn or should you sell the stuff you're not using (that much) anymore.
</p>

<p>
Vehicle horsepower is interesting, lets say the sanlford 6.2m wide plow requires 300hp vehicle to pull it, however if you use 300hp tractor it is slow as hell, it can barely pull it heh.
</p>


	<h2>Missions</h2>

<p>
When you click the blue circle with ! on it and click "something?" you can access the field by teleporting there. Now if you hit R key you open the missions dialog.
</p>

<p>
Missions dialog is tasks from the AI farmers which you can do for them for cash payment. This is great way to test new random equipment and get some cash. You have time limit to accomplish the mission and you cannot leave the area, even making long turns will trigger the zone inside which you must stay.
</p>

<p>
Missions freeze the normal gameplay, so if you have crops growing, vehicles moving (helper or courseplay etc), they will stay frozen until you have exited chosen mission. Missions do not work in multiplayer for this same reason. Sorry to burst your bubble about missions, they should have been running along with the normal "simulation" but they are not. Missions feel almost like cheating, time freezes and you can make crazy amounts of money.
</p>

<p>
Honestly, why not just use money cheat to get the money and save your boredom time. I hate to be blunt but missions are really gimmicky and hacky things, yeah you get the field prices cheaper when you get better reputation with the owners, but that is the only point. I don't like cheating but I rather add 100k cash from savegame file than sit around with the other simulation (your fields and vehicles) frozen in time when you are wasting your time in mission.
</p>

	<h2>Market Prices</h2>

<p>
Market prices gets updated constantly. When you sell grain the market price for that type immediately drops after the sale (this is why if you can use the train its great to get huge load).
</p>

<p>
Great demand means the price stays the same no matter how much you sell, however the price might not be the highest in the market.
</p>

<p>
In short; if possible stock up with different type of grains and sell when the market looks great for that type. Of course stocking up is not necessarily always possible due silo size and money requirement.
</p>


	<h2>Animals</h2>

<p>
Caretaking for animals is feeding, water and cleaning. Equipment you bare minimum need are; water tank trailer and basic grass cutter.
</p>

<p>
Equipment list, you dont need everything it depends on which animals you keep and what is your preferred caretaking (play) style.
</p>
<ul>
<li>Telehandler, to pick up food spills</li>
<li>Mower, to cut grass</li>
<li>Forage Wagon, to pickup and transport grass</li>
<li>Water Tank, to pickup and supply water</li>
</ul>

<p>
Water can be picked up from any water source in the map. Drive the tanker trailer into the water edge and you get option to refill the tank. Deploy the water for animals near the water location which usually is marked on the map.
</p>

<p>
Grass will be cut with the mower, then collected by the forage wagon which is used to dump grass to the animals.
</p>

<p>
Cleaning can be done with various telehandlers or front loaders, or even by forage wagon drive by (heh). There is also modding option, if you are lazy or cant be bothered with pesky stuff like taking care of your animals, there is <a href="mods.php#FS17_AnimalTableManners">mod called "FS17_AnimalTableManners"</a> which disables this whole cleanliness hassle.
</p>

<p>
There is no limit on animals you can have, however game will only render limited number of them (not sure what this number is).
</p>

<p>
<b>Chickens</b> require no caretaking.
</p>

<p>
<b>Sheep</b> give them water and grass. Clean the food spills.
</p>

<p>
<b>Pigs</b> give them water, straw, 50% base food (corn), 25% grain (wheat or barley), 20% protein (canola or sunflowers or soybeans) and 5% root crops (potatoes or sugar beet). Clean the food spills.
</p>

<p>
<b>Cows</b> give them water, straw, 20% grass (grass), 50% base food (silage or hay) and 30% power food (power food). Clean the food spills.
</p>

<p>
Cows Total Mixed Ration (TMR). You can use mixing wagon like siloking. Use telehandler to lift bales, you need dry hay, sileage and straw. When you get into mixing wagon pulling vehicle, you can see the TMR values in your controls (F1) menu. You can create sileage bales from grass with baler and bale wrapper.
</p>

<p>
Bales can be placed directly to the cow feeding area as food.
</p>

<p>
You can turn hay directly into silage by baling hay and using bale wrapper.
</p>

<p>
You can unload tons of straw or food into the spot it goes and animals use it as they need it, this way you don't have to keep refilling it so often. But please be tidy, don't just unload huge load of it where the animal food/straw trigger wont reach.
</p>


	<h2>Gameplay Styles</h2>

