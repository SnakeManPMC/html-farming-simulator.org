	<h2>Terrain Design Tips FS17</h2>

<p>
Fences kill enjoyable farming game-play.
</p>

<p>
For those dark nights put enough lights to your farm and especially on the grain bin areas where trucks are coming in with high speed.
</p>

<p>
Make fields small but leave large open area around it so players can enlargen them by ploughing if they want. However do remember that American landscapes usually have fields right up to road edges, just a ditch between them depending on the location, so its not necessarily realistic american farming design if you leavy huge gaping empty areas between your fields themselves or fields and roads.
</p>

<p>
Make fields small but symmetrical so players can merge them if they want mega large fields.
</p>

<p>
Place large enough vehicle sheds to store huge fleet of implements and vehicles, you dont want to end up all road side grass areas littlered with parked vehicles.
</p>

<p>
Decoration tips; few grain bins, maybe seed / fertilizer bins as well and then one barn or vehicle shed type building just like FS17 Parkers Prairie terrain near the animals.
</p>

<p>
Near fields its good to have small open areas so you can park your vehicles there, like when you are not taking them back to the farm yard vehicle sheds. But please remember the note about realistic american farming locations.
</p>

<p>
Nick Welker from Welkers Farm INC said that in real life fields do NOT have a 90 degree square corners, watch <a href="https://youtu.be/1ZIGS8eE6xQ?t=2760" target="_blank">his comments on youtube video</a>. So its more work for you as terrain dev but if you want realism, add round(er) corners for your fields.
</p>

