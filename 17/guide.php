	<h2>Crops Growth</h2>

<p>
It seems like the crops growth cycle for one stage is about 6 hours game time (not real time) but this depends on your difficulty and crop growth settings. So if you time accelerate to 120x this time will pass quicker. Unfortunately there seems not to be any hard facts about the hours it takes to grow.
</p>

<p>
For large fields its recommended to use crop growth normal or slow because you can be still seeding the field when other end of it is already switched to first growth stage. Once you get your farm with 10+ large fields, then I would recommend you setting the growth rate to slow permanently, its just best to keep it there as you dont have time even with courseplay running several seed hawks to keep up with the growth on normal or fast option.
</p>

<p>
Crop growth is really weird, it is not counted reasonably as you really often get the one half of the field switched to first stage while you are still seeding other half of the field and I am not talking about even 20 hectare field here! Weird.
</p>

<p>
<i>2018-10-07 speculation:</i> if you use the mod "Fast Forward" which allows you to do 12,000x time acceleration, it <i>might be</i> (and here is where the speculation comes into play) that the crops growth cycle gets out of whack. I tested this today on Cornbelt huge 8km terrain which alone might cause some issues (speculation, uuh speculation!), at least if you hit CTRL-R default key for this mods 12,000x time acceleration and keep it going the crops do not grow. When you release the key back to real time scale and then wait for 30-60 seconds (seems to depend a little) then the crops grow into the stage according to how much time has passed, for example if you sped up the time for several days then the next growth cycle change goes instantly to the fully grown crop stage (and if you have it enabled, crops wither as well).
</p>

<p>
On 8km terrain Cornbelt I often times noticed that when crop growth rate was normal and I used "fast forward" 12,000x time accelleration, my fields kind of "lagged" in the stages, for example: I finished seeding F45 and was starting to seed F44 right next to it, well it was 2100hrs so I fast forwarded to 0500hrs and then started to seed... guess what happens? After I've not done even the first headland... the already seeded parts switch to first stage of growth, hehe it was hilarious to complete the headland and uh oh whats this, crops are already growing!? hehe <img src="../images/smileys/icon_smile.gif" alt="Smiley :)" loading="lazy">
</p>

<p>
So very simplistic way of looking at trouble free crop growth rate is this: set it from options to SLOW and do not time accelerate, at all. OR if you have to skip through the night, then wait in 1x normal game time at least five (5) real life minutes to make sure the next growth rate is not "lagging behind" and being switched when you are doing something with the field.
</p>

	<h2>Seed Pallets</h2>

<p>When your sower / seeder runs out of seeds, you need to refill it. On starter easy level there are several seed pallets in your farm, but these run out and you have to purchase more.
</p>

<p>The shop has them for sale under Pallets. This will drop a pallet outside the shop in town. You then have to pick up the pallet and transport it back to your farm. For a single pallet the best way to transport it is to purchase a <b>front loader attachment</b> and the <b>pallet forks</b>. These can be attached to the front of any of your starting tractors. Once you have that you can pick up the pallet and move it. Or you could use the universal auto loader truck trailer mod which automatically loads bales and pallets.
</p>

<p>
Note: the refill radius of these pallets / sowers and fertilizers is extremely short, in most cases you can't make courseplay courses working with the pallets you have placed on the ground. Tip: use auger wagon or actual seed tender to refill your implements.
</p>


	<h2>Seed Placeables</h2>

<p>
You can purchase placeable objects for seeds and fertilizer. These will always supply you with seeds / fertilizer without the need to purchase more and handle them, it even costs <i>less</i> than pallets. These placeables charge solid fertilizer 0.95 per liter while stock and modded pallets cost 1.6 per liter. These placeables are <a href="https://www.farming-simulator.com/mod.php?lang=en&country=us&mod_id=52699&title=fs2017" target="_blank">Official mod from Gians (developers of FS17)</a> and highly recommended.
</p>

<p>
When you purchase these, take into careful consideration where you place them. Do you plan on buying more than one set of these? If not, then its good idea to place them into location which is easily accessible and central location on the map, however on large terrains 4km or 8km you definitely want to place more of these.
</p>


	<h2>Seed Tenders</h2>

<p>
Once you get larger fields and run huge operations lets say using Big Bud and Seed Hawk setups, you run into the problem of getting seeds and fertilizer out into the field because nobody wants to drive huge and slow tractor back to the shop for refill.
</p>

<p>
Use auger wagons to fill seed carts on the field. Drive auger wagon next to a seed / fertilizer silo and refill, then open the auger wagon pipe and drive next to the seed cart and it will unload automatically. This is excellent method of keeping those Big Bud Seed Hawk setups satisfied on the fields as nobody wants to drive 32km/h Big Bud into the silos for a refill.
</p>

<p>
There are also specific seed tender mods, but frankly when you can use auger wagon like this why bother, however <a href="https://www.google.com/search?q=farming+simulator+17+seed+tender" target="_blank">google search seed tender</a> if you like.
</p>

	<h2>Train</h2>

<p>In Goldcrest terrain there are three massive buildings that can be accessed by the train. They are not marked locations on the map and can't be teleported to. Each one is a freely-available silo with a 500K capacity that can hold all grain crops and root crops.
</p>

<p>The contents of these three silo buildings can be loaded or unloaded from any trailer or train car. Even without the train, each is a useful and free secondary storage location, but the storage figures in the menu don't include these silos, so if you stash some grain there remember that you did it.
</p>

<p>To load or unload a train, move a grain wagon (all three are mostly the same) under the pipe visible overhead, the discharge grate is lined up with it so loading and unloading are in the same spot.
</p>

<p>To load or unload a trailer from these buildings, look on the far side away from the tracks. There is a section of workshop that you can drive through, that's where the drop grate is, and there's a silo structure outside, that's where you can load. Despite appearances these sites will take potatoes and sugar beets.
</p>

<p><i>I use a train on big fields for 'field bin' storage - use an auger and you can move the train up the line while you unload. From the train you have several 500t hubs. At those hubs you can unload the train and sell to points all over the map as well as sell at Maplefield direct,</i>
</p>

<p><i>Learn the routes, the hubs and the truck/tipper access and you'll find they are a huge benefit to large game play.</i>
</p>

<p><i>Train is very useful. Can sell HUGE loads during great demands without the drop in price you would get between tipper loads.</i>
</p>

<p><i>Can also use it as intermediate storage as you harvet fields. Park a train close to the field being harvested, dump combine into auger wagon and unload directly into traincar...no need to run to silos.</i>
</p>

	<h2>Trailer</h2>

<p>Not a movie trailer ;-) but trailer which you hinch with tractor or car etc and you load various items on top of it for easy transport.
</p>

<p>For example seed pallets are ideal to be loaded into trailer in front of the shop and then driven to your farm.
</p>

<p>You need forklift to lift the stuff into a trailer.
