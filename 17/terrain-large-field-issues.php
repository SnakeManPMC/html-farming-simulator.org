	<h2>Terrain Large Field Issues FS17</h2>

<p>
HUGE fields have issues, what are those? <img src="../images/smileys/icon_surprised.gif" alt="Smiley :o" loading="lazy">
</p>

<p>
Terrain with huge fields have issue with <b>FS17 Courseplay</b>, it wont generate field work courses. I tried it to 1,565 hectare field and FS17 Courseplay refused to generate no matter what kind of settings. When you generate course for 1,000 hectare field it freezes up your FS17 so ALT-TAB is not working, it takes several seconds to save on i7 4790k CPU computer with an SSD.
</p>

<p>
Large / huge fields take a long time (real gaming time, not depending on time accelleration) for the the crops to grow, it takes several days for wheat harvest to grow. At least I think this is abnormal behavior, usually it takes pretty much overnight to grow crops heh dunno cant remember.
</p>

<p>
FS17 Courseplay generates routes for 1,008 hectare fields. Note: it takes several seconds when it does this while it freezes the game, be patient and WAIT. Also do not, I say again, do NOT save such insanely large courseplay route because next time you start the terrain savegame it takes several minutes to start if not ultimately locking up your game. Do not save huge FS17 Courseplay courses, no matter the field sizes.
</p>

<p>
Also purchasing large / huge fields seem to freeze FS17, this is reproduceable on PMC 16384 terrain with the 1,475 hectare field number 30. However this might have been some bug in the field too <img src="../images/smileys/icon_wink.gif" alt="Smiley ;)" loading="lazy">
</p>

<p>
Any solution for FS17 Courseplay freezing field work course generation on a large field? Yeah, record field outline for a huge field by dividing the field to two or perhaps even four smaller sections, then use this field outline in FS17 Courseplay to generate field work course. For example I used this method several times on FS19 PMC Grande Gardens 16km with 500ha and above size fields, I believe I used it on my <a href="stories-start-from-zero-fs17-cornbelt.php">FS17 Cornbelt Start From Zero</a> savegame as well, cant be sure. For every problem there is a solution <img src="../images/smileys/icon_wink.gif" alt="Smiley ;)" loading="lazy">
</p>

