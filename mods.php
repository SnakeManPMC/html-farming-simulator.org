RIGHT-SHIFT-F = Disengage 'FollowMe'<br>
RIGHT-SHIFT-H = Wait/Resume<br>
RIGHT-SHIFT-W/S = Distance increase/decrease<br>
RIGHT-SHIFT-A/D = Offset left/right adjust<br>
