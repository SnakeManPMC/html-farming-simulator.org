	<h2>Giants Editor Preferences Viewport Tab FS25</h2>

<p>
In Giants Editor when you use file -&gt; preferences as shown below.
</p>

<img src="screenshots/Giants-Editor-File-Preferences.jpg" class="imgresponsive" loading="lazy" alt="Giants Editor File Prefrences" title="Giants Editor File Prefrences">

<p>
Then click the viewport tab and you see this.
</p>

<img src="screenshots/Giants-Editor-Preferences-Viewport-Tab.jpg" class="imgresponsive" loading="lazy" alt="Giants Editor Preferences Viewport Tab" title="Giants Editor Preferences Viewport Tab">

<p>
<b>Navigation</b> translation scale by default 0.35 and rotation scale by default 0.004 values.
</p>

<p>
<b>Manipulation</b>
</p>

<p>
Gizmo Sie by default 10.
</p>

<p>
Frame Gizmo Size by default 2.
</p>

<p>
Frame Radius Scale by default 0.04.
</p>

<p>
CV Pick Radius by default 1. I have to assume "CV" means control vertex or vertice whichever term is proper one. Control vertices are the points in a spline or polygon shape.
</p>

<p>
Frame Distance Scaling by default 1.
</p>

<p>
Terrain Editing Preview on/off, by default off.
</p>

<p>
Painting / Sculpting Brush Step Size by default 1.
</p>

<p>
Interactive Placement Scale Variance by default 0. For random tree/vegetation (not that there are any bushes but still) placement it is essential that decent number is used here, do not ever under any circumstances place "clone army of trees" all with the default scaling and rotation etc, too many people across FS17, FS19 and FS22 have done that mistake and ruined their otherwise perfectly good terrains. This feature is for the CTRL-B interactive object placement.
</p>

<p>
Show Delta Manipulation on/off, by default off.
</p>

<p>
Show Translation Plane on/off, by default off.
</p>

<p>
Translation Place Subdivisions, a drop-down menu with multiple values to choose from, by default 5.
</p>

<p>
Translation Plane Spacing, a drop-down menu with multiple values to choose from, by default 1.
</p>

<p>
Max Spline Curvature by default 1.
</p>

<p>
<b>Grid</b> enable on/off, by default off. Subdivisions by default 24. Spacing by default 1.
</p>

<p>
<b>Rendering</b>
</p>

<p>
Clip Object Mask, by default ff.
</p>

<p>
Antialiased Lines on/off, by default off.
</p>

<p>
Outline Selection on/off, by default on.
</p>

<p>
Wireframe Selection on/off, by default off.
</p>

<p>
Background Color. Click the big box next to this text to bring up a background color picker dialog.
</p>

