<p>
Next up was just waiting until such time the installer can be downloaded. My previous FS22 Install and Release story begins from hours before serial key goes active on release day, so I have no reference data for how many days/weeks prior to release day the installer becomes available for download, cant remember if giants announced it somewhere or did it just one day appear in eshop. Anyways. Now I'm waiting, perhaps check eshop every few days.
</p>

