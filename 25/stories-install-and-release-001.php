	<h2>Release Date Announcement And Pre-Order</h2>

<p>
2024-06-25 Farming Simulator 25 (FS25) release date is announced for 2024-11-12 (November 12th, 2024).
</p>

<p>
At this time I didn't have too much free money to use for purchasing FS25 serial key, technically I did have it but Farming Simulator 22 (FS22) with its season passes which I purchased, gave me such a bad taste in mouth as those passes were completely useless to me, only thing I could use from those was Kubota tractor and one sprayer, now that is overkill if anything if this whole season pass thing only gets you one tractor and one implement, barely, I really don't need those but as they are available I do use them in early savegames.
</p>

<p>
For this reason I was extremely reluctant to buy FS25, my enthusiasm for it was just about zero. I don't think on june 25th community even had any info about the new game engine yet so it was just best guesses and speculation, which was founded upon the previous game release ie FS22 and as I said that left my mouth with a very bad after taste with useless season pass and all.
</p>

<p>
I had already decided year or so ago that for the next game engine release I will not buy any of the DLC or whatever season passes, I will rather buy them more expensive when I see something interesting being confirmed than just throwing money blindly to Giants Software like gambling that maybe the season pass will have some good stuff in it. Nope, not me, not this time around, FS22 burnt me pretty bad.
</p>

<p>
Time went on, months passed by, in sep/oct was the dreaded PMC website hosting and domain registration yearly payment, renewal fees, for those I saved money for months, actually I might have already been in saving mode on back in jun 25th just cant remember. I wrote details about this project in <a href="https://www.pmctactical.org/forum/viewtopic.php?t=22666" target="_blank">PMC Tactical Forum "PMC websites from shared hosting to Virtual Private Server" topic</a>, but in short I finally managed to get all those website fees paid off, but now I am broke as ever and have a debt to pay.
</p>

<p>
Early september while dealing with that website payment stuff I decided to ask community for help on getting me FS25, gifted as the term goes. Went and posted <a href="https://www.pmctactical.org/forum/viewtopic.php?t=23009" target="_blank">PMC Tactical Forum "Farming Simulator 25 Game Gift to PMC" topic</a> about it. Then again time went on and honestly I was quite surprised that nobody took upon that gifting idea, seeing how much for example people in twitch and youtube live streams just throw away serial key "give-aways" like they are nothing, I have to admit I was quite sad/disappointed that nobody offered a serial key for me, especially as I laid out what would I do "in return" for the whole community (read the topic, its all there).
</p>

<p>
Finally one guy stepped up, PMC Tactical Forum member <a href="https://www.pmctactical.org/forum/memberlist.php?mode=viewprofile&u=12345" target="_blank">coop_63</a>, he gifted a copy of FS25 to me. Man that was awesome! Thank you <b>coop_63</b> you are so generous, thank you so much! <img src="../images/smileys/icon_biggrin.gif" alt="Smiley :D" loading="lazy">
</p>

