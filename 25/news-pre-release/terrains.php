	<h2>Preview Interview Hutan Pantai</h2>

<p>
Preview and Interview Hutan Pantai terrain with Horia Serban. -source <a href="https://www.farming-simulator.com/newsArticle.php?news_id=574" target="_blank">Giants website blog</a>.
</p>

<img src="screenshots/Hutan-Pantai-Terrain-Preview-Logo.jpg" class="terrainimg" loading="lazy" alt="FS25 Hutan Pantai Terrain Farming Simulator 25 Screenshot" title="FS25 Hutan Pantai Terrain Farming Simulator 25 Screenshot">

<p>
<i>
Welcome to Hutan Pantai, where the harmony of the East Asian countryside meets the vibrancy of modern cities - a symbiotic relationship, as Associate Producer and Technical Artist Horia Serban calls it. Learn more in our interview as he explains what he and his team of environmental artists envisioned when creating the terrain.
</i>
</p>

<a href="screenshots/Hutan-Pantai-PDA-Map.jpg" target="_blank"><img src="screenshots/thumbs/Hutan-Pantai-PDA-Map.jpg" class="terrainimg" loading="lazy" alt="FS25 Hutan Pantai Terrain Farming Simulator 25 Screenshot" title="FS25 Hutan Pantai Terrain Farming Simulator 25 Screenshot"></a>

<img src="screenshots/Interview-Associate-Producer-Horia-Serban.jpg" class="terrainimg" loading="lazy" alt="FS25 Hutan Pantai Terrain Farming Simulator 25 Screenshot" title="FS25 Hutan Pantai Terrain Farming Simulator 25 Screenshot">

<p>
<i>
Horia, tell us a bit about you and your position?
</i>
</p>

<p>
<i>
I'm an Associate Producer for the Environment Department here at GIANTS Software, and I also work as a Technical Artist. I've been in the gaming industry for about 20 years now, and for the past 8, I've had the pleasure of working at GIANTS. My journey here started with Farming Simulator 17 back in 2016, and I immediately connected with the unique aspects of the game. Before joining, I worked on a variety of titles, like Call of Duty: Ghosts, Dead Rising 3, and Test Drive Unlimited 2, mostly in outsourcing.
</i>
</p>

<p>
<i>
But it was with Farming Simulator that I found a new passion – this game is special to me, and being part of its growth and evolution has been truly exciting. Watching the franchise expand over the years and knowing I've played a role in that journey brings me so much happiness and pride. I'm thrilled to help bring this world to life for our players, season after season.
</i>
</p>

<a href="screenshots/Hutan-Pantai-Terrain-20.jpg" target="_blank"><img src="screenshots/thumbs/Hutan-Pantai-Terrain-20.jpg" class="terrainimg" loading="lazy" alt="FS25 Hutan Pantai Terrain Farming Simulator 25 Screenshot" title="FS25 Hutan Pantai Terrain Farming Simulator 25 Screenshot"></a>

<p>
<i>
Inspiration and Atmosphere
</i>
</p>

<p>
<i>
What kind of atmosphere were you aiming for?
</i>
</p>

<p>
<i>
With the Hutan Pantai terrain, we wanted to create an authentic East Asian atmosphere that embodies the harmony between countryside landscapes and the vibrant, modern cities typical of this region. This symbiotic relationship—where the bustling cities are sustained by the hard work and dedication of the rural areas—is central to the terrain's vision. The contrast and connection between these two areas were essential elements for us to capture.
</i>
</p>

<p>
<i>
We also wanted to integrate specific elements unique to the region, showcasing how the modernization of cities has facilitated the movement of goods. Through features like elevated highways and suspended train bridges, we connect the fields to the urban centers, creating a seamless flow that mirrors real-world infrastructure. Our goal was to present a world that feels both familiar and rich with the unique cultural blend of East Asia.
</i>
</p>

<p>
<i>
In addition, we made it a priority to preserve the heritage and culture of the region by designing a traditional village and a temple area, both with architectural styles that have been in use for hundreds of years. These elements honor the historical and cultural richness of the area, ensuring authenticity and a sense of legacy.
</i>
</p>

<a href="screenshots/Hutan-Pantai-Terrain-14.jpg" target="_blank"><img src="screenshots/thumbs/Hutan-Pantai-Terrain-14.jpg" class="terrainimg" loading="lazy" alt="FS25 Hutan Pantai Terrain Farming Simulator 25 Screenshot" title="FS25 Hutan Pantai Terrain Farming Simulator 25 Screenshot"></a>

