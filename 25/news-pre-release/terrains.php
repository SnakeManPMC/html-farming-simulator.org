<p>
<i>
Creation and Challenges
</i>
</p>

<p>
<i>
Every new terrain design starts with extensive research focused on the specific area or theme we're aiming to recreate. We dive deep into the culture and unique characteristics of that region to bring the most authentic experience to our players. Our approach involves splitting up tasks within the environment department to ensure we gather as much relevant information as possible.
</i>
</p>

<p>
<i>
Some of our team members focus on researching the local architecture, while others concentrate on understanding the vegetation, farming practices, and community-specific elements we need to capture. We also pay close attention to details that we should avoid to ensure the terrain is respectful and enjoyable for our community.
</i>
</p>

<p>
<i>
Once the research phase is complete, we move into the prototyping or pre-production stage. Here, we experiment by gray-boxing, creating basic 3D models to establish the layout of the terrain. This stage allows us to test and adjust the positioning of essential elements like production buildings and landmarks. It's an exciting process of turning research into a tangible environment, and it helps us set a strong foundation for the final terrain.
</i>
</p>

