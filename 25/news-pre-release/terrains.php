<p>
<i>
What were some of the challenges in the creation of the terrain?
</i>
</p>

<p>
<i>
Creating the Hutan Pantai terrain brought its own set of challenges, especially with the complexity of the city. This was beyond anything we'd done before, so we had to carefully balance authenticity with performance. We wanted the city to feel real and dynamic without taking up too much of the terrain's surface area or impacting performance on consoles and PCs. Achieving that balance was a significant technical and creative challenge.
</i>
</p>

<p>
<i>
Another major challenge was designing the infrastructure to connect the city with the rural areas. Building a highway that seamlessly links the main city to the rest of the terrain required meticulous planning, and adding tunnels to the route brought another layer of complexity. Ensuring this infrastructure was both functional and visually integrated into the landscape was a true test of our design and engineering skills.
</i>
</p>

