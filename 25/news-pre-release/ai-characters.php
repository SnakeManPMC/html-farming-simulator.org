	<h2>Animal Farmer Katie</h2>

<p>
<i>After traveling the world, Katie came home and took over the animal farm from her family. She is a hands-on person and can explain the basics of livestock farming. If you want to know more about her, she can share some personal stories and anecdotes from her travels.</i>
</p>

<p>
<i>Most of her anecdotes include animals in some way, and some of her stories are just myths she collected here and there. She's not always serious.</i>
</p>

<p>
<a href="screenshots/AI-Character-Animal-Farmer-Katie-01.jpg" target="_blank"><img src="screenshots/thumbs/AI-Character-Animal-Farmer-Katie-01.jpg" class="terrainimg" loading="lazy" alt="AI Character Farming Simulator 25 Screenshot" title="AI Character Farming Simulator 25 Screenshot"></a>
