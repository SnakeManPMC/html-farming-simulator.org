	<h2>Adding Characters & Guidance</h2>

<p>
<i>We wanted to make the towns a bit more lively, so you can meet a handful of virtual people, brought to life by real voice actors. Each character has their own personality and area of expertise. Because we were able to create the characters without drawing developer resources from other game features, we added them as a side bonus.</i>
</p>

<p>
<i>To provide especially new virtual farmers some touching points in the form of guidance, they offer some advice and help for the farming basics. If you want to know more about them, you can just casually talk to them and see what spontaneously comes to their mind. Sometimes they might ask what you think.</i>
</p>

<p>
<i>They may also need a helping hand and offer you some contract when there's something to be done in the neighborhood.</i>
</p>


	<h2>Grandpa Walter</h2>

<p>
<i>As is traditional in farming, the farm is passed down from generation to generation. In this case, however, Grandpa Walter isn't passing the farm on to his son, who would normally be next in line, but wanted to explore another life away from the countryside.</i>
</p>

<p>
<i>Walter will share some of his experience and tell you more about the history of the family farm, and send you to his trusted, old friend and helper if you require assistance for modern agriculture.</i>
</p>

<p>
<a href="screenshots/AI-Character-Granpa-Walter-01.jpg" target="_blank"><img src="screenshots/thumbs/AI-Character-Granpa-Walter-01.jpg" class="terrainimg" loading="lazy" alt="AI Character Farming Simulator 25 Screenshot" title="AI Character Farming Simulator 25 Screenshot"></a>
</p>

<p>
<i>I’m doin’ this for a while now. Despite all the modernization, there are still farmers sitting on tractors, right? My own grandfather, yes I had one too, he was old-school. And I mean it in the actual definition.</i>
</p>

<p>
<i>He did everything on horseback. Plowing, sowing, getting to town and delivering his produce to the market. As did most of the others. Horses were the backbone of it all.</i>
</p>

<p>
<i>Back then, a significant portion of his farm was about horse breeding and selling strong, healthy mares and stallions to other farmers.</i>
</p>

<p>
<i>Then, tractors became a thing. Slowly, at first. Then, more and more horses were retired from plowing duty.</i>
</p>


	<h2>Helper Ben</h2>

<p>
<i>Having been the trusted friend of Walter's for more than 30 years, Ben can tell beginners more about the farming basics. How to sow and harvest crops, operate the machines for the various field working steps, and more. He's always near at the starting farm.</i>
</p>

<p>
<a href="screenshots/AI-Character-Helper-Ben-01.jpg" target="_blank"><img src="screenshots/thumbs/AI-Character-Helper-Ben-01.jpg" class="terrainimg" loading="lazy" alt="AI Character Farming Simulator 25 Screenshot" title="AI Character Farming Simulator 25 Screenshot"></a>
</p>

<p>
<i>Well, since your grandfather slowed down, he didn't really bother with the latest machines and did a lot of stuff... well, in an "old school" kind of way.</i>
</p>

<p>
<i>That's probably why he sent you to me.  I'm an avid reader of all those agricultural magazines, you know. Old, but still learning...</i>
</p>

<p>
<i>If you come to me, I can help you make sure everything grows well in the fields.</i>
</p>


	<h2>Neighbor David</h2>

<p>
<i>Neighbor David is not a farmer, but tries to be. He's a beginner. He made some mistakes on the way and will warn others not to repeat them. In return, you may tell him, that it would be better for him to learn operating a baler instead of opening a hotel with wellness treatments, offering people to sleep on fresh hay - which he heard from others is a thing.</i>
</p>

<p>
<i>Additionally, being from the city, he can explain his perspective on both city life and living in the country and how he learned to really appreciate farming when he put his hands into the soil himself.</i>
</p>

<p>
<a href="screenshots/AI-Character-Neighbor-David-01.jpg" target="_blank"><img src="screenshots/thumbs/AI-Character-Neighbor-David-01.jpg" class="terrainimg" loading="lazy" alt="AI Character Farming Simulator 25 Screenshot" title="AI Character Farming Simulator 25 Screenshot"></a>
