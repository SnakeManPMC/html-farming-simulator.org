<p>
<i>Aren’t you a curious one!</i>
</p>

<p>
<i>When I was reading about medieval farming, I was reminded that I really loved our farm, and the country life surrounded by nature, animals and livestock.</i>
</p>

<p>
<i>So I changed my major to agricultural sciences and only minored in history. Can’t escape your families' legacy sometimes, huh?</i>
</p>

<p>
<i>When I finished college, I traveled the world and worked on farms to see how they were doing things in other countries and cultures.</i>
</p>


	<h2>Lumberjack Noah</h2>

<p>
<i>Noah is the local forester and lumberjack. His main profession is taking care of the forest, making sure it's handled with respect. If needed, he can explain the forestry basics.</i>
</p>

<p>
<i>But he prefers to talk more about woodcarving, even though his well-crafted objects are usually not as practical as he thinks, and you are free to point out flaws in his designs.</i>
</p>

<p>
<i>Since he also likes detective stories, he will pursue anyone who leaves an empty can of Fizzy Sprizz lemonade in the forest. Prepare to get a preventative lesson on how to behave in the woods.</i>
