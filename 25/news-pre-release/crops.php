	<h2>Rice</h2>

<p>
There are two different types - long grain rice and what we just call "rice". -source <a href="https://www.farming-simulator.com/newsArticle.php?news_id=563" target="_blank">Giants website blog</a>.
</p>

<a href="screenshots/Rice-Growth-Stages-01.jpg" target="_blank"><img src="screenshots/thumbs/Rice-Growth-Stages-01.jpg" class="terrainimg" loading="lazy" alt="Rice Planting And Harvesting Farming Simulator 25 Screenshot" title="Rice Planting And Harvesting Farming Simulator 25 Screenshot"></a>

<a href="screenshots/Rice-Long-Growth-Stages-01.jpg" target="_blank"><img src="screenshots/thumbs/Rice-Long-Growth-Stages-01.jpg" class="terrainimg" loading="lazy" alt="Rice Planting And Harvesting Farming Simulator 25 Screenshot" title="Rice Planting And Harvesting Farming Simulator 25 Screenshot"></a>

<p>
Rice and long grain rice need different water levels, machines to sow and harvest, and seeds, of course. Long grain rice isn't sown in water like the other one. Instead, the rice paddy for long grain rice gets flooded AFTER the sowing, not before.
</p>

<a href="screenshots/Build-Menu-Rice-02.jpg" target="_blank"><img src="screenshots/thumbs/Build-Menu-Rice-02.jpg" class="terrainimg" loading="lazy" alt="Rice Planting And Harvesting Farming Simulator 25 Screenshot" title="Rice Planting And Harvesting Farming Simulator 25 Screenshot"></a>

<p>
Both types of rice are planted or sown in rice fields (also called rice paddies) - that's a special type of field deepened to allow for water retention. That's why you require open space on your land.
</p>

<p>
Via the build menu, you start the creation process. Instead of just placing down a field, you can shape it in any size or form.
</p>

<a href="screenshots/Build-Menu-Rice-01.jpg" target="_blank"><img src="screenshots/thumbs/Build-Menu-Rice-01.jpg" class="terrainimg" loading="lazy" alt="Rice Planting And Harvesting Farming Simulator 25 Screenshot" title="Rice Planting And Harvesting Farming Simulator 25 Screenshot"></a>

