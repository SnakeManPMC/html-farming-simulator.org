<p>
Its rather odd that AI Hired Worker has the option to turn off headlands, I mean why would you want to do that? You want to drive headlands yourself? Why, I just don't get it, why not drive the whole field then if you like driving yourself manually, what I mean is that if you disable headlands then AI Hired Worker will not finish the field, it only does the inner rows and leaves headland untouched. In fact if you have crop destruction on, once hired worker is finished you most likely will get some tires driving over a crop destroying it. So overall, options are always good but I don't understand why someone would not do headlands <img src="../../images/smileys/icon_confused.gif" alt="Smiley :?" loading="lazy">
</p>

<p>
Here djgoham confirmed that Steering Assist (GPS) works so that you still have to control gas and break, like hitting cruise control to drive forward and then hitting back/break button (S key) at the end of the row to stop. In the corners, end of rows, you have to drive the vehicle yourself to turn it, just like with GPS mod all these years.
</p>

<p>
Steering Assist indeed feels very much like a GPS mod on these game-play preview videos, functionality appeares to be the same.
