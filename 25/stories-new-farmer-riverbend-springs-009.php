<p>
PMCTODO F08 was prepared with a disc harrow on top of ... hmm was it growing or ready to harvest cotton crop, which turned it into stubble tillage field stage or type rather, this is the almost FS22 looking brown field ground texture. Now when you use direct drill planter on stubble tillage there is so subtle change on the shading of the field texture that you can just barely see it, in fact I would say that you cant see the difference on a quick glance, you have to be really careful when checking where have you planted before. Hmm not sure how to avoid this, meaning stubble tillage which was done by disc harrow (fermet softer 11 ps) when ran on top of a existing crop. Well one way is to use a deep cultivator, that generates that traditional beautiful black dirt ground texture for fields. But other than that... yeah you might be in world of hurt with this kind of annoying field ground texture shade issue, so be aware.
</p>

